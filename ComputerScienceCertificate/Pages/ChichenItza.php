	<h2>A romváros legérdekesebb épületei</h2>
	<h3>Kastély – El Castillo</h3>
	<p>
		Chichén Itzá legimpozánsabb épülete a spanyol hódítók által El Castillonak, azaz „Kastély”nak elnevezett és Kukulkán tollas 
		kígyóisten tiszteletére emelt piramistemplom. A templomot Quetzalcoatl, a toltékok királya emeltette 987 körül. A pontosan 
		észak-dél-kelet-nyugati tájolású, hatalmas, kilenc teraszra tagolódó, négyzetes kőtemplom 55 méter magas. Minden oldalán 91-91 
		lépcsőfok – mindösszesen 365 – vezet fel a tetőn álló szentélyhez, „ahol a legyőzött ellenség szívét kivágva mutattak be győzelmi 
		áldozatot”. A templom különlegessége a tavaszi és őszi napéjegyenlőség idején feltáruló tünemény: a kelő és nyugvó Nap sugarai 
		által vetett árnyékok egy, a templomból előkúszó kígyó alakját elevenítik meg; a fejet és a farkat a piramis alján és tetején 
		kőből faragták ki.
	</p>
