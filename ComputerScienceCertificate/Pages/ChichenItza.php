		lakói visszaköltöztek és 879-től ismét fejlődésnek indult. Az itt épült maja templomváros kiterjedése körülbelül egy kilométer 
		volt; lakói kizárólag maja főpapok és magas funkcionáriusok voltak. A alacsonyabb rangú hivatalnokok és földművesek a város 
		körüli síkságot népesítették be és innen látták el a városlakókat.
