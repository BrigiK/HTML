<p>
	Petra egy romváros Jordániában. A sivatag sziklái között megbúvó egykori karavánközpont egy arab néptörzs, a nabateusok 
	virágzó fővárosa volt. A 7. századtól hanyatlásnak indult, fokozatosan elnéptelenedett és csupán a beduin pásztorok tudtak 
	létezéséről. Utolsó európai látogatói a keresztes lovagok voltak, majd 1812-ben Jean Louis Burckhardt svájci felfedező egyik 
	útja során ismét rátalált. A sziklából kifaragott város romjai, hatalmas sírtemplomai és varázslatos természeti környezete 
	egyedülálló komplexumot alkotnak.
</p>

<h2>Fekvése és jelentősége</h2>
<p>
	Védett fekvése és jó vízellátottsága révén egy mesterséges oázis épült itt – Petra. A település az i. e. 5. és az i. sz. 3. század 
	között fontos kereskedelmi csomópont volt. A városba vezető mély és szűk Szik szurdok – 1600 méter hosszú, egyes helyeken mindössze 
	2 méter széles, meredek falai pedig 80-90 méter magasak (a mai látogatók is csak gyalog, esetleg ló-, vagy teveháton jutnak el ide) – 
	védelmet biztosított. A sziklák között fakadó patak vize– amelyet a legendák szerint Mózes fakasztott egyetlen botütéssel, amikor 
	Egyiptomból kivonulva népével megpihent itt – az életet biztosította.
</p>

<h2>Története</h2>
<p>
	A város története szorosan összefonódik a nabateusok történelmével. Eredeti írásos emlék alig maradt ránk, így a nép és a város 
	történetét közvetett források, a Biblia, valamint görög és római irodalmi, történelmi és földrajzi művek leírásai, illetve utalásai 
	alapján állíthatjuk össze.
</p>

<img src="..\Images\kincs fent.jpg" height="500" class="displayed">

<p>
	A régészeti kutatások eredményei szerint az Akaba és a Holt-tenger között elterülő szakadékokkal tagolt völgyterület 
	időszakosan, már a kőkorszak (I. e. 9. évezred) óta lakott volt. A Biblia alapján a térséget I. e. 1500 körül a hurrik 
	majd az edomiták népesítették be.
</p>

<p>
	Petra i. e. 200 – i. sz. 100 között élte virágkorát. A fővárosban – a sivatagi nomád népek szokásainak továbbfejlesztésével, 
	egyedi építészeti stílust teremtve – a nabateusok a kőfalakból faragták ki elegáns, monumentális épületeiket, templomaikat, 
	fürdőiket, lakóházaikat és sírboltjaikat. Tökélyre fejlesztették fazekas, kőfaragó és ciszternaépítő tudományukat. A sziklák 
	között fakadó patak vizét ötletes felszíni és föld alatti csatornákból és víztározókból álló rendszerrel vezették a városba, 
	hogy a rendelkezésre álló víz minden cseppjét megőrizzék.
</p>

<p>
	1517-től 1918-ig az egész Közel-Kelet oszmán fennhatóság alatt állt, és e négyszáz év során az Oszmán Birodalom peremvidéke 
	teljes jelentéktelenségbe süllyedt. A keresztes háborúk után az 1800-as évek elejéig egyetlen európai sem jutott el Petrába. 
	1812-ben Johann Ludwig Burckhardt svájci felfedező és arabista volt az első, aki a csaknem feledésbe merült várost újra ismertté 
	tette. Több mint 100 évvel később, 1916-ban az első világháború alatt a brit katonai érdekek szolgálatában itt harcoló Arábiai 
	Lawrence látogatott el ismét Petrába.
</p>
<p>
	Petra a Közel-Kelet kedvelt turistalátványossága. A modern turizmus az 1930-as években kezdődött, ekkoriban épültek az első 
	szállodák, néhány éppen a romvárosban, közvetlenül a római színház mellett. (Ezeket a városképet romboló épületeket azóta 
	visszabontották.). Petra hatékonyabb turisztikai hasznosítása érdekében 1968 és 1985 között a jordán kormány kiköltöztette a 
	területről a B’dúl beduintörzs lakóit, akik Petra árnyas sziklaüregeit lakásként használták. Ma e törzs tagjai a Vádi Músza falvaiban 
    élnek; nagy részük éppen Petrában dolgozik idegenvezetőként.

</p>

<h2>Látnivalók</h2>
<p>
	A Vádi Músza („Mózes völgye”) varázslatos természeti környezete és az emberi kéz alkotásai harmonikusan olvadnak össze. A Biblia 
	szerint Mózes itt fakasztott vizet a sziklából a szomjazó zsidóknak, akik a Kánaán felé tartottak. A völgy homokkőből épül fel, 
	a sziklában található ásványi anyagok a kőzet felületén kioldódnak és különböző színűre festik.
</p>

<img src="..\Images\szinek.jpg" height="500" class="displayed">

<p>
	A domináns rózsaszín, vöröses színt a vas, a sárgát a kén, a zöldet a réz, a fehéret az agyag, a szürkét és feketét a magnézium 
	váltja ki. Ott, ahol sziklás völgy kiszélesedik, 34 monumentális és 600 egyszerűbb sírt, továbbá sziklába vájt lakóházakat és 
	oszlopos szabadtéri szentélyeket, áldozati helyeket alakítottak ki. A városban mintegy 800 történelmi emlékmű van. Ezek az építmények 
	nabateus, görög és római stílusjegyeit viselik magukon. A három kultúra stílusjegyei gyakran keverednek, vagy együttesen jelennek meg, 
	néha egyetlen építményen belül is.
</p>

<h3>Szik</h3>
<p>
	A tektonikus mozgás eredményeként kialakult Szik, vagy más írás szerint „SIQ” szurdokon át vezet az út Petrába. A Szik 1,2 km 
	hosszú és néhol 80-100 méter magas, szélessége mindössze 2-3 méter.
</p>

