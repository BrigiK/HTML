<h3>Kincsesház</h3>
<p>
	A Szik szurdok végénél áll a leghíresebb petrai építmény, a 40 méter magas, 25 méter széles hellenisztikus stílusú Haznet 
	al-Faraún, azaz a „Fáraó kincstára”, impozáns, színpadi díszletre emlékeztető homlokzata. A sziklafalba vájt gigantikus 
	fülkébe illeszkedő kétszintes homlokzatot Alexandriából eredő építészeti elemek, faragványok és frízek díszítik. A falakon 
	görög mitológiai jeleneteket ábrázoló féldomborművek láthatók. Az alsó szint hatoszlopos portikuszát oromzat koronázza. A felső 
	szintet egy szálkőben álló áthidaló gerendára épített kúp alakú tholosz zárja, amelynek tetején egy urna áll, az oldalsó elemek 
	tört oromzatot formáznak. 
</p>

<img src="..\Images\kincses.jpg" height="500" class="displayed">

<p>
	A teljes építmény nagyszerű állapotban maradt fenn, kivéve az urnát, amely szétesett, amikor a beduinok – abban a reményben, 
	hogy megszerezhetik a fáraó elrejtettnek vélt kincsét – rálőttek. A 19. századi festmények alapján a szabadon álló oszlopok 
	egyike valamikor leomlott, amelyet a 20. században állítottak helyre. A hagyomány szerint az épületet III. Ramszesz építette, 
	akinek bányái voltak a környéken. E fáraó azonban az i. e. 12. században élt, míg a Haszné az i. e. 1. század első feléből 
	származik, és IV. Aretasz építtette.
</p>

<p>
	A 2010-es években a Szikhez csatlakozó egyik oldalág lejtése alapján feltették, hogy a Szik aljzata egykor mélyebben lehetett. 
	A feltevés alapján indított ásatás egy teljes szintet tárt fel az eddig ismert építmény alatt, ahol 16 személy csontvázát rejtő 
	sírkamrákat találtak. Ekkor derült ki, hogy IV. Aretasz volt az építtető, az itt eltemetett személyek valószínűleg közeli 
	hozzátartozói, és az eddig ismert felső szint az áldozati helység lehetett, ahol a halottkultuszt ápolták.
</p>

