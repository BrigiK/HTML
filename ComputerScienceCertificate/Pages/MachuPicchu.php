       <h2>Leírása</h2>
       <p>
       	Machu Picchu egy kövezett út mentén terült el, amely részét képezte annak a 40 000 kilométeres hálózatnak, 
       	amely átszelte az egész hatalmas Inka Birodalmat, a hegyektől a tengerpartig. A „naputak” általában 
       	4-6 méter szélesek voltak, és két oldalukon fal húzódott, ám Machu Picchu esetében szándékosan nehezen járható 
       	nyomvonalat jelöltek ki, amelynek több pontjáról is félelmetes szakadék tárul az ember szeme elé. A sziklafalakba 
       	rögzített kőlapokból álló úton csak libasorban lehetett járni. A településhez felvezető, utolsó szakasz előtt egy 
       	függőhídon kellett áthaladni; ez egyértelműen jelzi, hogy a városlakók számára a biztonság elsődleges szempont volt. 
       	Erre utal a „Katonai bástya” vagy „Laktanya”, amely a város bejáratánál, délkeleten, egy 150 fokos hosszú lépcsősor 
       	tetején áll. A négyszögletes építménynek öt trapéz alakú ablaka és sok belső fülkéje volt.
       </p>
       <p>
       	Machu Picchu délkeleti szektora körül 5 méter magas, egy méter széles kőfal húzódik. A falból nyíló, hatalmas kőtömbökből 
       	és egy nagy (egyetlen tömbből faragott) architrávból álló kapun keresztül lehetett bejutni a városba, a mezőgazdasági 
       	célokra használt teraszok között. A kaput csapok, fatörzsek és reteszek segítségével csak belülről lehetett zárni, 
       	illetve nyitni. A várost azonban nem a fal védte, hanem maga a természet: a folyóvölgy felé karéjként három 700 méteres 
       	szakadék, a negyediken egy magasba szökő hegycsúcs alkotta a valódi védelmet.
       </p>
       
