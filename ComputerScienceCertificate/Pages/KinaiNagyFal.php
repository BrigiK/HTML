	<h2>A fal szerkezete</h2>
	<p>
		A nagy fal nagy része hegyvonulatokon húzódik úgy, hogy a külső oldalon meredek sziklák vannak.
	</p>
	<p>
		A Ming-kori fal külső borítása téglákból és tégla alakú kődarabokból áll. Belsejét döngölt agyaggal, illetve kőzúzalékkal 
		töltötték fel. 
	</p>
	
	<img src="..\Images\kinainagyfal.jpg" width="500" class="displayed">
	
	<p>
		Magassága átlagosan 10 méter, szélessége a talapzatánál 7-8 méter, tetejénél pedig 4-5 méter.
	</p>
	<p>
		A falon nyíllövésnyi távolságra egymástól bástyákat, erődítményeket és az ellenség esetleges támadásaira füsttel és 
		tűzjelekkel figyelmeztető jelzőtornyokat építettek. A bástyákban, illetve az erődítményekben élelmiszert és fegyvereket 
		tároltak. A falon és közelében csapatok állomásoztak, hogy időben jelezzék a külső támadásokat és a védelem első vonalául 
		szolgáljanak.
	</p>
	
	<h2>A fal jelenlegi állapota</h2>
	<p>
		A fal egy kis részét restaurálták, elsősorban a turisták miatt. Nagy része azonban az enyészetnek van kitéve, 
		s így bizonyos részek mára már szinte eltűntek. A fal napjaink vandalizmusának is áldozatává válik, például kedvelt 
		célpontja a graffitiseknek. A fal tégláinak egy részét a helyi lakosok hordják el, hogy azokból építkezzenek. A Ming-kori 
		falnak egyes becslések szerint 37%-a, más becslések szerint 42%-a tűnt el. A fal más részei a szabálytalanul működő bányák 
		miatt omlanak le. Egyes turisták kíméletlen magatartása, az új fejlesztési projektek elmaradása és a műemlékvédelem pénzhiánya 
		is fokozza a problémát.
	</p>
	
