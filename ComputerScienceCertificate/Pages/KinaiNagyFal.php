	<h2>Az űrből láthatóság legendája</h2>
	<p>
		A közhiedelemmel ellentétben a nagy fal nem látható szabad szemmel a világűrből. Csak néhány méter széles, és a 
		színe hasonlít a környezetéhez, így az űrből lehetetlen észlelni. Az ezzel kapcsolatos városi legenda (hogy a nagy 
		fal volna az egyetlen ember alkotta dolog a földön, ami a Holdról is látszik) egy 1932-es amerikai képregényből ered. 
	</p>
	
