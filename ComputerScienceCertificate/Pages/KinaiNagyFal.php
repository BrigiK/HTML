	<h2>A fal építése</h2>
	<p>
		A nagy fal valójában soha nem volt összefüggő fal, különböző erődítményekből állt, amelyeknek jellege és elhelyezkedése a
		mindenkori politikai helyzettől függően változott. Az első védelmi rendszert Csin Si Huang-ti kezdte kiépíteni az i. e.
		210-es években a Hadakozó fejedelemségek korának korábbi falszakaszait felhasználva. A későbbi dinasztiák némelyike új
		erődítményeket húzott fel, míg más korszakokban a falat egyáltalán nem használták. A ma látható
		falszakaszok az 1368 és 1644 között uralkodott Ming-dinasztia idején épültek; ezeket már téglából emelték, de a
		korábbi erődítmények fő építőanyaga a döngölt föld volt.
	</p>
	<p>
		Mivel a fal nem összefüggő, egységes építmény, hanem különböző korokban, egy több száz kilométer széles sávban épült
		falszakaszok összessége, pontos hosszát nem lehet megállapítani. Az egyik forgalomban lévő hivatalos adat a fal
		hosszát 6352 km-re teszi, de más becslések 3000-10 000 km között ingadoznak. A Ming-kori fal legkeletibb végpontja
		a Sanhajkuan átjáró a Pohaj-öböl partján, legnyugatibb vége pedig a Csia Jü-kuan átjárónál található Kanszu tartományban.
	</p>
