	<p>
		Egy 2009-ben közzétett eredmény szerint a kínai nagy fal 290 kilométerrel hosszabb, mint eddig gondolták, majd 2012-ben is 
		egy újabb 100 km-es szakaszára bukkantak a mongol határnál. Kína északnyugati területén, Hszincsiang közelében, archeológusok 
