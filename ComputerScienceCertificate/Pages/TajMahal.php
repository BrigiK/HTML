	szerkezet köti össze őket, ezen a fő alapzat, vagyis a sírszerkezet vörös kőtalapzata támaszkodik meg.
</p>

<h2>Társadalmi jelentősége</h2>
<p>
	A sah udvarában számtalan nő élt, akik jelentős kulturális hatást gyakoroltak az uralkodó környezetére. Maga a síremlék a 
	női sírokkal, tekinthető egyben a nőiesség emlékművének is, és ezt a jelleget Mumtáz Mahal halálának évfordulóján évente 
	megrendezett ünnepségek is erőteljesen kihangsúlyozták. A szertartásrendet az első alkalommal – még az épület alapozása során – 
	Sáh Dzsahán írta elő, a későbbiekben ezt követték.
</p>
<p>
	Az első napon vallástudósok, udvari nemesek, gazdag polgárok, szegény, rászoruló családok ájtatos imákkal istentiszteletet 
	tartottak, miközben jótékony cselekedeteket hajtottak végre, lakomát rendeztek és szétosztottak mintegy 50 000 rúpiát az érdemesek 
	és a szegények között. Másnap ezt teljesen azonos módon megismételték – ez más síremlékek évfordulóin nem volt szokásos –, azonban 
	már az uralkodó és udvarhölgyei részvételével. Ekkor újabb 50 000 rúpiát osztottak szét. A női gyűlésen bárki részt vehetett, a 
	szükséget szenvedőket vendégül látták. A nagyszámú látogató tette szükségessé a mecset és a vendégház építését.
</p>

<h2>A komplexum</h2>
<p>
	Az egész Tádzs-komplexum több, észak-déli tengelyen elhelyezkedő, mintegy 900×300 méter összméretű teraszt foglal magába. 
	A déli oldalon terek, piacok, fogadók és lakóépületek, szakaszos nyílt terek váltják egymást, ez az egykori Mumtázábád. 
	Maga a mauzóleum a fallal körülvett, hosszúkás alaprajzú, hatalmas együttes fő tengelyének északi végén található, és a 
	látvány kedvéért a folyóra néz. Nem a kert (csár bág) középpontjában helyezkedik el, mint általában a kor építészeti megoldásainál 
	szokásos volt, hanem a folyó felőli oldalon, téglalap alakú talapzaton.
</p>

<img src="..\Images\bennn.jpg" height="500" class="displayed">

<p>
	A falak négy sarkán egy-egy nyolcszögletű pavilont építettek. A komplexum három fő egysége a tulajdonképpeni síremlék 
	és a hozzá tartozó kert a kiépített vízgyűjtőrendszerrel, valamint az ezektől délre fekvő két épületegyüttes, az egyik 
	az előudvar, a másik a karavánszerájok a bazárral.
</p>

<h2>A síremlék épületegyüttese</h2>
<p>
	Az épületcsoport a síremlékből, az ahhoz tartozó négy minaretből, valamint egy szimmetrikusan elhelyezkedő vendégházból  
	és egy mecsetből  áll, amelyek keletről és nyugatról szegélyezik a központi épületet. A síremlék márvány alapanyagát 
	Makranából, a melléképületekhez a homokkövet pedig a Vinthjan-hegységrendszerből hozták. A makranai márvány tulajdonsága, 
	hogy a ráeső fény színétől függően maga is változtatja a színét.
</p>

