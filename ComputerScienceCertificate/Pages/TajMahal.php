<p>
	A Tádzs Mahal (jelentése: „A paloták koronája”) az indiai Agrában, a Jamuna folyó partján található mauzóleum elnevezése, 
	amely valójában egy teljes épületkomplexumot takar, ennek része maga a fehér márvány síremlék is. Sáh Dzsahán mogul sah 1632 
	és 1647 között építtette 1631-ben, gyermekszülésben elhunyt szeretett felesége emlékére. Őt is itt helyezték örök nyugalomra.
</p>
<p>
	A Tádzs Mahal a mogul síremlék-építészet csúcspontja, amely nem csak annak stílusjegyeit viseli magán, hanem ötvözi azt a 
	helyi hagyományokkal is. Bár hatással volt későbbi síremlékek építésére is, az épület egyedi arányai, finom díszítettsége 
	utánozhatatlanná tette.
</p>
<p>
	A tökéletes arányaival feltűnő, nemes anyagokkal gazdagon díszített műemlékegyüttes nemcsak építészeti jelentőségű, hanem a hozzá 
	kapcsolódó legendák és történetek révén az örök szerelem emlékműveként is ismert.
</p>

<h2>Története</h2>
<p>
	Sáh Dzsahán sah kicsapongó természetű uralkodó volt, nem vetette meg az élvezeteket. Ugyanakkor igénye volt a kultúrára is, 
	a Mogul Birodalom nagy építtetőjeként tartják számon. Mumtáz Mahal a sah kedvenc felesége volt, aki 1631. június 17-én halt 
	meg Burhanpurban, 14. gyermeke születése során, miközben hadjáratba kísérte urát. Helyben temették el. Halálát követően a sah 
	búskomorságba esett, elhanyagolta a birodalom ügyeit. Elhatározta, hogy egyedülálló síremléket építtet számára, amely halála 
	után majd az ő nyughelye is lesz.
</p>
<p>
	A Tádzs Mahal építése 1632 januárjában kezdődött meg, Sáh Dzsahán történetírói szerint, az uralkodó által készített alaprajznak 
	megfelelően. A helyszín kiválasztása több mint hat hónapot vett igénybe. Sáh Dzsahánt nagy fájdalommal töltötte el, hogy emlékműve 
	nem fog örökké tartani, ezért mindent megtett annak érdekében, hogy az épület biztos lábakon álljon. Kortárs források rögzítették, 
	hogy milyen fából készültek a víz kiemelésére szolgáló kutak, hogyan cserélték ki a laza homokot kavicsra, és milyen, akkoriban 
	használt betonnal tették biztossá az alapot. Az eljárás olyan sikeres volt, hogy az épület azóta is ellenáll a földrengéseknek 
	és az árvizeknek, a természetvédőket is megdöbbenti az épület rendkívüli stabilitása. Alapjait a mogul hidraulikai építészet 
	elvei szerint fektették le, téglafalú üreges oszlopokra, amelyeket teljes szélességükben szálafából készült kerekekkel erősítettek 
	meg, egymástól egyenlő távolságra helyezve el őket az oszlopok belsejében. 
</p>

